def print_map(player, importMap):
    from os import system

    system('cls')

    # Sets the character's position to "@", replacing " ".
    importMap[player.getX()][player.getY()] = "@"

    lineCount = 0
    bCount = 0
    xCount = 0

    for i in range(len(importMap)):
        for j in range(len(importMap[i])):
            if importMap[i][j] == "B":
                bCount += 1
            if importMap[i][j] == "X":
                xCount += 1

    # Prints the map. Joins/formats each list item (-) with a space in between.
    # Each list is printed on it's own line, this just separates the individual items in each.
    print("--  Press ESC to exit.\n")
    print("\u250c" + "\u2500" * 23 + "\u2510")
    for k in importMap[player.getX() - 6:player.getX() + 6]:
        # --------------------------------------------------------------------------
        # Code only looks silly due to formatting the legend on the side of the map.
        # Basic version is the final else.
        # --------------------------------
        if lineCount == 1:
            print("\u2502" + " ".join(k[player.getY() - 6:player.getY() + 6]) + "\u2502", end="")
            print("\t    Map Legend:")
        elif lineCount == 2:
            print("\u2502" + " ".join(k[player.getY() - 6:player.getY() + 6]) + "\u2502", end="")
            print("\t\t\t\t\t  This Room:")
        elif lineCount == 3:
            print("\u2502" + " ".join(k[player.getY() - 6:player.getY() + 6]) + "\u2502", end="")
            print("\t\tO: Portal")
        elif lineCount == 4:
            print("\u2502" + " ".join(k[player.getY() - 6:player.getY() + 6]) + "\u2502", end="")
            print("\t\tX: Chest\t\t\tChests: {}".format(xCount))
        elif lineCount == 5:
            print("\u2502" + " ".join(k[player.getY() - 6:player.getY() + 6]) + "\u2502", end="")
            print("\t\tB: Boss Chest\t\t\tBosses: {}".format(bCount))
        elif lineCount == 6:
            print("\u2502" + " ".join(k[player.getY() - 6:player.getY() + 6]) + "\u2502", end="")
            print("\t     |+-#: Walls")
        elif lineCount == 9:
            print("\u2502" + " ".join(k[player.getY() - 6:player.getY() + 6]) + "\u2502", end="")
            print("\t\tH: HELP\t\t\t  P: SAVE GAME")
        elif lineCount == 11:
            print("\u2502" + " ".join(k[player.getY() - 6:player.getY() + 6]) + "\u2502", end="")
            print("\t\t\t        ")
        else:
            print("\u2502" + " ".join(k[player.getY() - 6:player.getY() + 6]) + "\u2502")
        lineCount += 1
    print("\u2514" + "\u2500" * 23 + "\u2518")
